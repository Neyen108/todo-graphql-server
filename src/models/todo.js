const { Schema, model } = require("mongoose");

const TodoSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
    completed: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

const Todo = model("todo", TodoSchema);

exports.Todo = Todo;
